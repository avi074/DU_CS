import math
print("Functions of Math Module\n")
print(" 1. Ceil(5.124705)   : %s"%(math.ceil(5.124705)))
print(" 2. Degrees(5.5)     : %s radian"%(math.degrees(5.5)))
print(" 3. Radians(315.13)  : %s degree"%(math.radians(315.13)))
print(" 2. Sin(90 deg)      : %s"%(math.sin(math.radians(90))))
print(" 3. Cos(pi)          : %s"%(math.cos(math.pi)))
print(" 4. Tan(pi / 3)      : %s"%(math.tan(math.pi/3)))
